package Model;

public class Button {
	private int width;
	private int height;
	
	public Button() {
		this.width = 14;
		this.height = 14;
	}
	
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	
	
}
